package kata

import (
	"testing"

	"github.com/stretchr/testify/assert"
)

func TestAccum(t *testing.T) {
	{
		result := Accum("ZpglnRxqenU")
		expect := "Z-Pp-Ggg-Llll-Nnnnn-Rrrrrr-Xxxxxxx-Qqqqqqqq-Eeeeeeeee-Nnnnnnnnnn-Uuuuuuuuuuu"
		assert.Equal(t, result, expect, "result doesn't equal expect")

	}
	{
		result := Accum("NyffsGeyylB")
		expect := "N-Yy-Fff-Ffff-Sssss-Gggggg-Eeeeeee-Yyyyyyyy-Yyyyyyyyy-Llllllllll-Bbbbbbbbbbb"
		assert.Equal(t, result, expect, "result doesn't equal expect")
	}
	{
		result := Accum("MjtkuBovqrU")
		expect := "M-Jj-Ttt-Kkkk-Uuuuu-Bbbbbb-Ooooooo-Vvvvvvvv-Qqqqqqqqq-Rrrrrrrrrr-Uuuuuuuuuuu"
		assert.Equal(t, result, expect, "result doesn't equal expect")
	}
	{
		result := Accum("EvidjUnokmM")
		expect := "E-Vv-Iii-Dddd-Jjjjj-Uuuuuu-Nnnnnnn-Oooooooo-Kkkkkkkkk-Mmmmmmmmmm-Mmmmmmmmmmm"
		assert.Equal(t, result, expect, "result doesn't equal expect")
	}
	{
		result := Accum("HbideVbxncC")
		expect := "H-Bb-Iii-Dddd-Eeeee-Vvvvvv-Bbbbbbb-Xxxxxxxx-Nnnnnnnnn-Cccccccccc-Ccccccccccc"
		assert.Equal(t, result, expect, "result doesn't equal expect")
	}
	{
		result := Accum("VwhvtHtrxfE")
		expect := "V-Ww-Hhh-Vvvv-Ttttt-Hhhhhh-Ttttttt-Rrrrrrrr-Xxxxxxxxx-Ffffffffff-Eeeeeeeeeee"
		assert.Equal(t, result, expect, "result doesn't equal expect")
	}
	{
		result := Accum("KurgiKmkphY")
		expect := "K-Uu-Rrr-Gggg-Iiiii-Kkkkkk-Mmmmmmm-Kkkkkkkk-Ppppppppp-Hhhhhhhhhh-Yyyyyyyyyyy"
		assert.Equal(t, result, expect, "result doesn't equal expect")
	}

}

